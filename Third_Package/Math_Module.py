def add(a, b):
  return a + b
def subtract(a, b):
  return a - b
def multiply(a, b):
  return a * b
def divide(a, b):
  if b == 0:
    return "Error: Division by zero"
  return a / b
def remainder(a, b):
  if b == 0:
    return "Error: Division by zero"
  return a % b
def integer_divide(a, b):
  if b == 0:
    return "Error: Division by zero"
  return a // b
def power(a, b):
  return a ** b
def square(a):
  return a ** 2
def square_root(a):
  return a ** 0.5
def cube(a):
  return a ** 3
def cube_root(a):
  return a ** (1/3)
def absolute(a):
  return abs(a)
